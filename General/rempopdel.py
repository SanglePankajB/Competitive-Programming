

# Remove, Pop, Del

lst = [1,3,2,4,5,7,8]

lst.remove(3)      # it will remove 3
print(lst)

del lst[1]        # similar to pop(value)
print(lst)

lst.pop(3)        # remove at index 3
print(lst)

lst.pop()         # remove from last
print(lst)


